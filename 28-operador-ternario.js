// Operador ternario: En JS es una forma corta, elegante y moderna de escribir una estructura if-else.

// Se llama ternario porque utiliza tres operandos

// Estructura: condision ? valorSiVerdadero : valorSiFalso

// Ejemplo1:

let age = 43;
let message = age >= 18 ? "Eres mayor de edad" : "Eres menor de edad";

console.log(message);

// Ejemplo2:

let userName = "Edwar";
let greet = userName ? `Hola, ${userName}` : `Hola, invitado`;

console.log(greet);